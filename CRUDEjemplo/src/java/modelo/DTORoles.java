package modelo;


public class DTORoles {
    int Id_roles;
    String Nombre;

    public DTORoles() {
    }

    public DTORoles(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getId_roles() {
        return Id_roles;
    }

    public void setId_roles(int Id_roles) {
        this.Id_roles = Id_roles;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

 
   
}
